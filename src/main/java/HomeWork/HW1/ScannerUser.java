package HomeWork.HW1;

public class ScannerUser {
    private  String name;
    private int age ;

    public ScannerUser() {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public int setAge(int age) {
        this.age = age;
        return age;
    }




}